     private void cargarRegistros(){
        File file = new File ("registroEstudiante.txt");
        BufferedReader reader = null;
        
        
 try {
            // FileReader reads text files in the default encoding.
            FileReader fl = new FileReader(file);

            // Always wrap FileReader in BufferedReader.
            reader = new BufferedReader(fl);
            String text;

            while((text = reader.readLine()) != null) {
                
             StringTokenizer st1 = new StringTokenizer(text, "%");
             int contador = 0;
             while(st1.hasMoreElements()){
                switch(contador){
                     case 0: jTextArea1.setText(jTextArea1.getText() + "\n" + "Codigo:   " + (String)st1.nextElement() + "\n");break;
                     case 1: jTextArea1.setText( jTextArea1.getText() + "\n" + "Nombre del Alumno:   " + (String)st1.nextElement() + "\n");break;
                     case 2: jTextArea1.setText( jTextArea1.getText() + "\n" + "Semestre:   " + (String)st1.nextElement() + "\n");break;
                     case 3: jTextArea1.setText( jTextArea1.getText() + "\n" + "Promedio:  " + (String)st1.nextElement() + "\n");break;
                     case 4: jTextArea1.setText(jTextArea1.getText() + "\n" + "Carrera:   " + (String)st1.nextElement() + "\n");break;
                     
               
                 }
                 contador++;
     
             }
             
            }
                }catch (FileNotFoundException e){
                    e.printStackTrace();
                } catch(IOException e) {
                    e.printStackTrace();
               
                }finally{
                    try{
                 if(reader!= null){

                 reader.close();}
                 
                 }catch (IOException e){}
                        }
                       }

